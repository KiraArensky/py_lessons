# 1. Проверка делимости числа
"""
Задание: Программа запрашивает у пользователя число и проверяет,
делится ли оно на 3 и 5 одновременно.
"""
num = int(input("Введите число: "))
if num % 3 == 0 and num % 5 == 0:
    print("Число делится на 3 и на 5.")
else:
    print("Число НЕ делится на 3 и на 5 одновременно.")
print()

# 2. Определение наибольшего из трёх чисел
"""
Задание: Запросите у пользователя три числа и определите, какое из них наибольшее.
"""
n1 = int(input("Введите первое число: "))
n2 = int(input("Введите второе число: "))
n3 = int(input("Введите третье число: "))
if n1 >= n2 and n1 >= n3:
    print("Наибольшее число:", n1)
elif n2 >= n1 and n2 >= n3:
    print("Наибольшее число:", n2)
else:
    print("Наибольшее число:", n3)
print()

# 3. Проверка логина и пароля
"""
Задание: Программа запрашивает у пользователя логин и пароль.
Если логин – "admin", а пароль – "1234", доступ разрешен.
"""
login = input("Введите логин: ")
password = input("Введите пароль: ")
if login == "admin" and password == "1234":
    print("Доступ разрешен.")
else:
    print("Ошибка доступа. Неверный логин или пароль.")
print()

# 4. Калькулятор с операторами присваивания
"""
Задание: Запросите у пользователя число и операцию (`+=`, `-=`, `*=`, `/=`), затем измените число.
"""
number = int(input("Введите число: "))
operation = input("Введите операцию (+=, -=, *=, /=): ")
value = int(input("Введите число для операции: "))
if operation == "+=":
    number += value
elif operation == "-=":
    number -= value
elif operation == "*=":
    number *= value
elif operation == "/=":
    if value != 0:
        number /= value
    else:
        print("Ошибка: деление на ноль.")
else:
    print("Ошибка: неверная операция.")
print("Результат:", number)
print()

# 5. Определение времени суток
"""
Задание: Запросите у пользователя текущий час (0-23) и определите время суток:
- Утро (6-11)
- День (12-17)
- Вечер (18-21)
- Ночь (22-5)
"""
hour = int(input("Введите текущий час (0-23): "))
if 6 <= hour < 12:
    print("Сейчас утро.")
elif 12 <= hour < 18:
    print("Сейчас день.")
elif 18 <= hour < 22:
    print("Сейчас вечер.")
else:
    print("Сейчас ночь.")
print()
