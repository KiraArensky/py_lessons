# 1. Подсчет суммы чисел (for)
"""
Задание: Программа запрашивает у пользователя число `n`
и считает сумму чисел от 1 до `n` включительно.
"""
n = int(input("Введите число: "))
sum_numbers = 0
for i in range(1, n + 1):
    sum_numbers += i
print("Сумма чисел от 1 до", n, "равна", sum_numbers)
print()

# 2. Поиск четных чисел в диапазоне (for)
"""
Задание: Программа выводит все четные числа в диапазоне от 1 до 20.
"""
for i in range(1, 21):
    if i % 2 == 0:
        print(i, end=" ")
print()

# 3. Вывод таблицы умножения (for)
"""
Задание: Программа запрашивает число `n` и выводит таблицу умножения для этого числа до 10.
"""
n = int(input("Введите число: "))
for i in range(1, 11):
    print(f"{n} x {i} = {n * i}")
print()

# 4. Подсчет количества гласных в строке (while)
"""
Задание: Программа запрашивает строку у пользователя и считает,
сколько в ней гласных букв (а, е, ё, и, о, у, ы, э, ю, я).
"""
text = input("Введите строку: ").lower()
vowels = "аеёиоуыэюя"
vowel_count = 0
i = 0
while i < len(text):
    if text[i] in vowels:
        vowel_count += 1
    i += 1
print("Количество гласных букв:", vowel_count)
print()

# 5. Поиск минимального и максимального числа в списке (while)
"""
Задание: Дан список чисел. Программа находит и выводит
наибольшее и наименьшее число в списке.
"""
numbers = [15, 3, 9, 21, 4, 7, 30, 12]
min_number = numbers[0]
max_number = numbers[0]
i = 1
while i < len(numbers):
    if numbers[i] < min_number:
        min_number = numbers[i]
    if numbers[i] > max_number:
        max_number = numbers[i]
    i += 1
print("Минимальное число:", min_number)
print("Максимальное число:", max_number)
print()
